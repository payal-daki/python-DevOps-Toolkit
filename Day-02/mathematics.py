import math

value = 2.7

# Square root
sqrt_value = math.sqrt(value)
print(f"Square Root: {sqrt_value}")  # Output: 1.6431676725154984

# Exponential
exp_value = math.exp(value)
print(f"Exponential: {exp_value}")  # Output: 14.879731724872837

# Logarithm
log_value = math.log(value)
print(f"Logarithm: {log_value}")  # Output: 0.9932517730102834
